private boolean patientExists(int patientID) {
        try {
            String sql = "SELECT COUNT(*) AS count FROM patients WHERE PatientID = ?";
            try (PreparedStatement ps = mrms_db.prepareStatement(sql)) {
                ps.setInt(1, patientID);
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        int count = rs.getInt("count");
                        return count > 0;
                    }
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error checking patient existence: " + e.getMessage());
            e.printStackTrace();
        }
        return false;
    }

 private String[] getPatientNames(int patientID) {
        String sql = "SELECT FirstName, LastName FROM patients WHERE PatientID = ?";
        try (PreparedStatement ps = mrms_db.prepareStatement(sql)) {
            ps.setInt(1, patientID);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    String firstName = rs.getString("FirstName");
                    String lastName = rs.getString("LastName");
                    return new String[]{firstName, lastName};
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error retrieving patient names: " + e.getMessage());
            e.printStackTrace();
        }
        return null;
    }
