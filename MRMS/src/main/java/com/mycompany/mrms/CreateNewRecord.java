    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // Retrieve data from text fields
        String diagnosis = jTextField3.getText().trim();
        String allergies = jTextField1.getText().trim();
        String immunizationHistory = jTextField4.getText().trim();
        String notes = jTextField2.getText().trim();
        String prescriptions = jTextField5.getText().trim();
        String motherName = jTextField8.getText().trim();
        String motherSurname = jTextField10.getText().trim();
        String motherDOB = jTextField12.getText().trim();
        String motherBloodType = jTextField6.getText().trim();
        String maternalHereditaryDisease = jTextField16.getText().trim();
        String fatherName = jTextField7.getText().trim();
        String fatherSurname = jTextField11.getText().trim();
        String fatherDOB = jTextField13.getText().trim();
        String fatherBloodType = jTextField9.getText().trim();
        String paternalHereditaryDisease = jTextField15.getText().trim();
        String patientBloodType = jTextField14.getText().trim();

        // Use the logged-in patient ID from Home.java
        int patientID = loggedInPatientID;

        // Check if the patient exists
        if (!patientExists(patientID)) {
            JOptionPane.showMessageDialog(null, "Patient with ID " + patientID + " does not exist.");
            return;
        }

        // Retrieve patient's first name and last name
        String[] patientNames = getPatientNames(patientID);
        if (patientNames == null) {
            JOptionPane.showMessageDialog(null, "Error retrieving patient's names.");
            return;
        }

        // SQL insert statement
        String sql = "INSERT INTO medicalrecords (FirstName, LastName, Diagnosis, Allergies, ImmunizationHistory, Notes, Prescriptions, "
        + "MotherName, MotherSurname, MotherDOB, MotherBloodType, MaternalHereditaryDisease, "
        + "FatherName, FatherSurname, FatherDOB, FatherBloodType, PaternalHereditaryDisease, "
        + "PatientBloodType, PatientID) "
        + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

        try (PreparedStatement pstmt = mrms_db.prepareStatement(sql)) {
            pstmt.setString(1, patientNames[0]); // FirstName
            pstmt.setString(2, patientNames[1]); // LastName
            pstmt.setString(3, diagnosis);
            pstmt.setString(4, allergies);
            pstmt.setString(5, immunizationHistory);
            pstmt.setString(6, notes);
            pstmt.setString(7, prescriptions);
            pstmt.setString(8, motherName);
            pstmt.setString(9, motherSurname);
            pstmt.setString(10, motherDOB);
            pstmt.setString(11, motherBloodType);
            pstmt.setString(12, maternalHereditaryDisease);
            pstmt.setString(13, fatherName);
            pstmt.setString(14, fatherSurname);
            pstmt.setString(15, fatherDOB);
            pstmt.setString(16, fatherBloodType);
            pstmt.setString(17, paternalHereditaryDisease);
            pstmt.setString(18, patientBloodType);
            pstmt.setInt(19, patientID);

            int rowsInserted = pstmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(null, "Medical record created successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to create medical record.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error creating medical record: " + ex.getMessage());
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButton2MouseClicked

