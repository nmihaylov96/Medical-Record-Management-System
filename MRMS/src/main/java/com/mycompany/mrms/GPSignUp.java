        private void connectDB() {
        try {
            Class.forName(DRIVER);
            mrms_db = DriverManager.getConnection(DATA_BASE,USRNM,PASWD);
            ps = mrms_db.prepareStatement("SELECT * FROM db_mrms");
            
            rs = ps.executeQuery();
            ResultSetMetaData data = rs.getMetaData();
            
            q = data.getColumnCount();
            
            while(rs.next()){
                Vector columnData = new Vector();
                
                for (i = 1; i <= q; i++){
                    columnData.add(rs.getShort("gpID"));
                    columnData.add(rs.getShort("gpFirstName"));
                    columnData.add(rs.getShort("gpMiddleName"));
                    columnData.add(rs.getShort("gpLastName"));
                    columnData.add(rs.getShort("gpEmail"));
                    columnData.add(rs.getShort("gpPassword"));
                    columnData.add(rs.getShort("gpPhone"));
                    columnData.add(rs.getShort("gpNationality"));
                    columnData.add(rs.getShort("gpCountry"));
                    columnData.add(rs.getShort("gpCity"));
                    columnData.add(rs.getShort("gpAddress"));
                    columnData.add(rs.getShort("gpPostCode"));
                    columnData.add(rs.getShort("gpDOB"));
                    columnData.add(rs.getShort("gpGender"));
                }
            }
    }    
        catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    

