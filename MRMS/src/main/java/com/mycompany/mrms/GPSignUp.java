    private void jSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSubmitActionPerformed
            // Perform validation before submitting the form
    if (jFirstName.getText().isEmpty() || jLastName.getText().isEmpty() ||
        jEmail.getText().isEmpty() || jReEmail.getText().isEmpty() ||
        jPassword.getPassword().length == 0 || jRePassword.getPassword().length == 0 ||
        jGender.getText().isEmpty() || // Check if gender is empty
        jCountry.getText().isEmpty() ||
        jCity.getText().isEmpty() || jAddress.getText().isEmpty() ||
        jDateOfBirth.getText().isEmpty()) {
        
        // Display an error message if any mandatory field is empty
        JOptionPane.showMessageDialog(this, "Please fill in all mandatory fields!");   
    } else {
    try {
        Class.forName(DRIVER);
        mrms_db = DriverManager.getConnection(DATA_BASE, USRNM, PASWD);
        
        // Prepare the SQL INSERT statement
        String sql = "INSERT INTO gp (gpFirstName, gpMiddleName, gpLastName, gpEmail, gpPassword, gpPhoneNumber, gpNationality, gpCountry, gpCity, gpAddress, gpPostCode, gpDOB, gpGender) " +
             "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        ps = mrms_db.prepareStatement(sql);

        // Set values for the prepared statement parameters
        ps.setString(1, jFirstName.getText());
        ps.setString(2, jMiddleName.getText());
        ps.setString(3, jLastName.getText());
        ps.setString(4, jEmail.getText());
        ps.setString(5, jPassword.getText());
        ps.setString(6, jPhone.getText()); // Make sure this matches the column name in your database
        ps.setString(7, jNationality.getText());
        ps.setString(8, jCountry.getText());
        ps.setString(9, jCity.getText());
        ps.setString(10, jAddress.getText());
        ps.setString(11, jPostCode.getText());
        ps.setString(12, jDateOfBirth.getText());
        ps.setString(13, jGender.getText());

        
        // Execute the INSERT query
        int rowsAffected = ps.executeUpdate();
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Registration Successful.");
        } else {
            JOptionPane.showMessageDialog(this, "Registration Failed.");
        }
    } catch (ClassNotFoundException | SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }
    }//GEN-LAST:event_jSubmitActionPerformed

