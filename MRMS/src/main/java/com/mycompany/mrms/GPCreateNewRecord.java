    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
    // Retrieve data from text fields
    String diagnosis = jTextField3.getText().trim();
    String allergies = jTextField1.getText().trim();
    String immunizationHistory = jTextField4.getText().trim();
    String notes = jTextField2.getText().trim();
    String prescriptions = jTextField5.getText().trim();
    String motherName = jTextField8.getText().trim();
    String motherSurname = jTextField10.getText().trim();
    String motherDOB = jTextField12.getText().trim();
    String motherBloodType = jTextField6.getText().trim();
    String maternalHereditaryDisease = jTextField16.getText().trim();
    String fatherName = jTextField7.getText().trim();
    String fatherSurname = jTextField11.getText().trim();
    String fatherDOB = jTextField13.getText().trim();
    String fatherBloodType = jTextField9.getText().trim();
    String paternalHereditaryDisease = jTextField15.getText().trim();
    String patientBloodType = jTextField14.getText().trim();

    // Use the logged-in GP ID from Home.java
    int GPID = loggedInGPID;

    // Check if the patient exists
    try {
        // Get selected patient from the combo box
        String selectedPatientFullName = (String) jComboBoxPatientsList.getSelectedItem();
        String[] names = selectedPatientFullName.split(" "); // Split full name into first name and last name

        // Query to find the PatientID based on FirstName and LastName
        String query = "SELECT PatientID FROM patients WHERE FirstName = ? AND LastName = ?";
        try (PreparedStatement pstmt = mrms_db.prepareStatement(query)) {
            pstmt.setString(1, names[0]); // First name
            pstmt.setString(2, names[1]); // Last name
            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                int patientID = rs.getInt("PatientID");

                // SQL insert statement for medical records
                String sql = "INSERT INTO medicalrecords (FirstName, LastName, Diagnosis, Allergies, ImmunizationHistory, Notes, Prescriptions, "
                        + "MotherName, MotherSurname, MotherDOB, MotherBloodType, MaternalHereditaryDisease, "
                        + "FatherName, FatherSurname, FatherDOB, FatherBloodType, PaternalHereditaryDisease, "
                        + "PatientBloodType, PatientID) "
                        + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

                try (PreparedStatement pstmtInsert = mrms_db.prepareStatement(sql)) {
                    // Set values for insert statement
                    pstmtInsert.setString(1, names[0]); // FirstName
                    pstmtInsert.setString(2, names[1]); // LastName
                    pstmtInsert.setString(3, diagnosis);
                    pstmtInsert.setString(4, allergies);
                    pstmtInsert.setString(5, immunizationHistory);
                    pstmtInsert.setString(6, notes);
                    pstmtInsert.setString(7, prescriptions);
                    pstmtInsert.setString(8, motherName);
                    pstmtInsert.setString(9, motherSurname);
                    pstmtInsert.setString(10, motherDOB);
                    pstmtInsert.setString(11, motherBloodType);
                    pstmtInsert.setString(12, maternalHereditaryDisease);
                    pstmtInsert.setString(13, fatherName);
                    pstmtInsert.setString(14, fatherSurname);
                    pstmtInsert.setString(15, fatherDOB);
                    pstmtInsert.setString(16, fatherBloodType);
                    pstmtInsert.setString(17, paternalHereditaryDisease);
                    pstmtInsert.setString(18, patientBloodType);
                    pstmtInsert.setInt(19, patientID);

                    int rowsInserted = pstmtInsert.executeUpdate();
                    if (rowsInserted > 0) {
                        JOptionPane.showMessageDialog(null, "Medical record created successfully.");
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to create medical record.");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selected patient not found in database.");
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error creating medical record: " + ex.getMessage());
        ex.printStackTrace();
    }

    }//GEN-LAST:event_jButton2MouseClicked

