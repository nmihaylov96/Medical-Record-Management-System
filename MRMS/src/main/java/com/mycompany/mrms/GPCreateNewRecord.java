    public void loadPatientsWithoutMedicalRecords() throws SQLException {
        try {
            String query = "SELECT p.FirstName, p.LastName " +
                           "FROM patients p " +
                           "LEFT JOIN medicalrecords m ON p.PatientID = m.PatientID " +
                           "WHERE m.PatientID IS NULL"; // Select patients without medical records

            PreparedStatement pst = mrms_db.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            while (rs.next()) {
                String firstName = rs.getString("FirstName");
                String lastName = rs.getString("LastName");
                model.addElement(firstName + " " + lastName);
            }

            jComboBoxPatientsList.setModel(model);

            pst.close();
            rs.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error loading patients list: " + e.getMessage());
            e.printStackTrace();
        }
    }

